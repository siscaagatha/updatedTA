package org.cis.optur.auto;

public class SP {
    int iterasi;
    int TO;
    double a;
    double b;
    int Nb;
    int TL;

    public SP(int iterasi, int TO, double a, double b, int nb, int TL) {
        this.iterasi = iterasi;
        this.TO = TO;
        this.a = a;
        this.b = b;
        Nb = nb;
        this.TL = TL;
    }
}
